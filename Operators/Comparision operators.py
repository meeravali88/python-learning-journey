#Comparision Operators in Python
x = 10  
y = 20
# Equal to operator
equal = (x == y)
# Not equal to operator
not_equal = (x != y)
# Greater than operator
greater_than = (x > y)
# Less than operator
less_than = (x < y)
# Greater than or equal to operator
greater_equal = (x >= y)
# Less than or equal to operator
less_equal = (x <= y)

print(equal)
print(not_equal)
print(greater_than)
print(less_than)
print(greater_equal)
print(less_equal)