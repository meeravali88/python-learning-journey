# bitwise operators in Python
a = 5 
b = 3 
# Bitwise AND operator
bitwise_and = a & b  
# Bitwise OR operator
bitwise_or = a | b  
# Bitwise XOR operator
bitwise_xor = a ^ b  
# Bitwise NOT operator
bitwise_not_a = ~a 
bitwise_not_b = ~b  

print(bitwise_and)
print(bitwise_or)
print(bitwise_xor)
print(bitwise_not_a)